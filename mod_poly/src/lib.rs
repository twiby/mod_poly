//! This is a custom made module for modular polynomial arithmetic.
//! It is made to be bound with Python

pub mod complex;
pub mod polynomial;

pub fn add(left: usize, right: usize) -> usize {
	left + right
}

#[cfg(test)]
mod tests {
	use super::*;

	#[test]
	fn it_works() {
		let result = add(2, 2);
		assert_eq!(result, 4);
	}
}
